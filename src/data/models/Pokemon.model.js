export class Pokemon {
  constructor({id, name, img, types, abilities, url, flavorText, evolutionLine, stats, weight, height}) {
    this.id = id
    this.name = name
    this.img = img
    this.types = types
    this.abilities = abilities
    this.url = url
    this.flavorText = flavorText
    this.evolutionLine = evolutionLine
    this.stats = stats
    this.weight = weight
    this.height = height
  }
}

